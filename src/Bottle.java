public class Bottle {
    String name;
    int year;
    double percentLeft;
    String country;
    public Bottle(){

    }

    public void setYear(int year) {
        this.year = year;
    }

    public void setName(String name){
        this.name = name;
    }

    public void setPercentLeft(double percentLeft){
        this.percentLeft = percentLeft;
    }

    public void setCountry(String country){
        this.country = country;
    }
}